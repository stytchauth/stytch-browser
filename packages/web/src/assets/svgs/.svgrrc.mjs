import path from 'node:path';
import replaceDarkFillAttribute from './babel-update-dark-fill-plugin.mjs';

const precision = 2;

function componentName(name) {
  // Remove the White/Black suffix and add Icon as a suffix
  return name.replace(/^Svg/i, '').replace(/(White|Black)$/i, '') + 'Icon';
}

/** @type {import('@svgr/core').Config} */
export default {
  jsxRuntime: 'classic',
  typescript: true,

  // This template differs from the default one so we can rename the icons and import
  // styles which are needed to use _styles.darkInvert
  // SVGR has a strange bug where comments at the start of the template are elided
  // so the autogenerated comment is after imports unfortunately
  // Default template: https://github.com/gregberge/svgr/blob/main/packages/babel-plugin-transform-svg-component/src/defaultTemplate.ts
  template: (variables, { tpl }) => {
    // language=JSX
    return tpl`
${variables.imports};

/* AUTOGENERATED: DO NOT EDIT */

import _styles from '../logo.module.css';

${variables.interfaces};

export const ${componentName(variables.componentName)} = (${variables.props}): JSX.Element => (
  ${variables.jsx}
);`;
  },

  // Default template: https://github.com/gregberge/svgr/blob/main/packages/cli/src/dirCommand.ts#L46
  indexTemplate: (paths) => {
    const exportEntries = paths.map(({ path: filePath }) => {
      const file = path.parse(filePath).name;
      // Convert to PascalCase
      const exportName = file
        .split(/\W+/g)
        .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
        .join('');

      // The only new thing here is we're using our own component name convention
      return `export { ${componentName(exportName)} } from './${file}'`;
    });
    return exportEntries.join('\n');
  },

  svgoConfig: {
    plugins: [
      {
        name: 'preset-default',
        params: {
          overrides: {
            // At our size we can remove quite a few decimal points without the logos being visually different
            cleanupNumericValues: { floatPrecision: precision },
            convertPathData: { floatPrecision: precision, transformPrecision: precision },
            convertShapeToPath: { floatPrecision: precision },
          },
        },
      },
    ],
  },

  jsx: {
    babelConfig: {
      plugins: [replaceDarkFillAttribute],
    },
  },
};
